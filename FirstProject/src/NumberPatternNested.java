
public class NumberPatternNested {

	public static void main(JavaString[] args) {
		
		
		for(int i =1;i<=5;i++)
		{	
			 System.out.println("");
		      for(int j=1;j<=i;j++)
		    	  System.out.print(j);

		}     
		
		for(int m=4;m>=1;m--)
		{	
			 System.out.println("");
			  for(int n=1;n<=m;n++)
				  System.out.print(n);
				  
		}     
	}

}
