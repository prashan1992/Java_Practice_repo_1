
public class NumberPatternSymmatric {

	public static void main(JavaString[] args) {
		
		
		for(int i=1;i<=5;i++)
		{
			int j=1;
			System.out.println("");
			
		       for(;j<=i;j++)
		       {   
		    	   System.out.print(j);
		          
		       }   
		      
		       for(j-=2;j>=1;j--)
			    	  System.out.print(j);
		       
		       
		}

	}

}
